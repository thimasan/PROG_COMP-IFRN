raio = float(input('Digite um valor de 1 a 100 centímetros para determinar o raio da circunferência:'))

area_circ = 3.14 * raio ** 2
comp_circ = 2 * 3.14 * raio

print(f'A área da circunferência é igual a {area_circ} centímetros quadrados e o comprimento da circunferência é igual a {comp_circ: .2f} centímetros ')