base = float(input('Por favor informe um valor para a base de um quadrilatero?'))
altura = float(input('Por favor informe um valor para a altura de um quadrilatero?'))

#CONDIÇÃO PARA ANALISE DO QUADRILATERO

if base == altura :
    print('Esse quadrilatero é um QUADRADO!')
else:
    print("Esse quadrilatero é um RETÂNGULO")